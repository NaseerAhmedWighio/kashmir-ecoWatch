  districts: string[];
  protectedAreas: string[];
  seasonality: string;
  threats: string[];
  conservationMeasures?: string[];
  sensitivity: 'low' | 'medium' | 'high' | 'critical';
  imageUrl?: string;
  verifiedSightings?: number;
  relatedSpecies?: string[];
  references?: string[];
  
